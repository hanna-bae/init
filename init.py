# import the streamlit
import streamlit as st 

# Introduce the Streamlit
def main():
    # set up the title 
    st.title('Hello world!')

    # set up the header 
    st.header('Header')

    # set up the subheader 
    st.subheader('Subheader')


if __name__ == '__main__':
    main()


